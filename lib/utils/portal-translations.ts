const translations: Record<PortalLocale, PortalTranslations> = {
  en, fr, es, ar, zh, hi, pt, ur, pa, tl, ko, ja, vi, fa,
  tr: tr_lang, ru, bn, ta, de, it,
}

/**
 * Get the full translation object for a locale.
 * Falls back to English if the locale is unknown.
 */
export function getTranslations(locale: PortalLocale = 'en'): PortalTranslations {
  return translations[locale] ?? translations.en
}

/**
 * Simple template string interpolation.
 * Replaces `{key}` placeholders with values from the `vars` object.
 */
export function t(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`))
}

/**
 * Get a translated status label.
 */
export function getStatusLabel(status: string, locale: PortalLocale = 'en'): string {
  const tr = getTranslations(locale)
  const map: Record<string, string> = {
    empty: tr.status_needed,
    pending_review: tr.status_uploaded,
    accepted: tr.status_accepted,
    needs_re_upload: tr.status_re_upload_needed,
    rejected: tr.status_rejected,
  }
  return map[status] ?? tr.status_needed
}

/**
 * Get a translated person role label.
 */
