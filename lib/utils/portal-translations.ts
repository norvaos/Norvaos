/** RTL languages  -  used to set dir="rtl" on the portal */
export const RTL_LOCALES: PortalLocale[] = ['ar', 'ur', 'fa']

export function isRtl(locale: PortalLocale): boolean {
  return RTL_LOCALES.includes(locale)
}

